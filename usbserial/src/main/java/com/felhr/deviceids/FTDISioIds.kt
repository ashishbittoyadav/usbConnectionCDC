    private fun isMicrochipFtdi(dev: UsbDevice): Boolean {
        if (dev.vendorId != 0x04d8 || dev.productId != 0x000a) return false
        for (i in 0 until dev.interfaceCount) {
            val intf = dev.getInterface(i)
            if (intf.interfaceClass == 0xff && intf.interfaceSubclass == 0xff && intf.interfaceProtocol == 0x00) return true
        }
        return false
    }

    fun isDeviceSupported(dev: UsbDevice): Boolean {
        return Helpers.exists(ftdiDevices, dev.vendorId, dev.productId) ||
                isMicrochipFtdi(dev)
    }

    @JvmStatic
    fun isDeviceIdSupported(vendorId: Int, productId: Int): Boolean {
        return Helpers.exists(ftdiDevices, vendorId, productId)
    }
